#include "ft_printf.h"

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	if (!str)
		write(1, "(null)", 6);
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}
